// Any (Cualquier dato)

// TODO: Cambiar el Any por el tipado correspondiente. 

let disney: any; 
disney = 'Star Wars y Marvel'; 
console.log(disney); 

disney = 15; 
console.log(disney); 

disney = true 
console.log(disney); 

disney = undefined 
console.log(disney); 

disney = null; 
console.log(disney); 

/*
    Any nos permite indicarle cualquier tipo de dato al valor de una misma variable.
    En TypeScript no se recomienda usarlo, en caso de necesitar usar algo es mejor usar JavaScript directamente. 
*/